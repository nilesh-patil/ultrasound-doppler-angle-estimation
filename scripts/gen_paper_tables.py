# Header banner stamped into every generated .tex (so nobody hand-edits them).
BANNER = (
    "% AUTO-GENERATED by scripts/gen_paper_tables.py from results/ — DO NOT EDIT.\n"
    "% Re-run: pixi run python scripts/gen_paper_tables.py\n"
)


def _label(mapping: dict, key: str) -> str:
    return mapping.get(key, key)


def _fmt(x: float, nd: int = 2) -> str:
    return f"{float(x):.{nd}f}"


def _wrap_table(body: str, caption: str, label: str, colspec: str) -> str:
    """Wrap a booktabs body in a float-free ``table`` environment."""
    return (
        BANNER
        + "\\begin{table}[htbp]\n"
        + "  \\centering\n"
        + f"  \\caption{{{caption}}}\n"
        + f"  \\label{{{label}}}\n"
        + f"  \\begin{{tabular}}{{{colspec}}}\n"
        + "    \\toprule\n"
        + body
        + "    \\bottomrule\n"
        + "  \\end{tabular}\n"
        + "\\end{table}\n"
    )


# --------------------------------------------------------------------------- #
# Table 1: replication (paper-protocol reproduction, frozen backbones).
# --------------------------------------------------------------------------- #
def replication_table(metrics: pd.DataFrame) -> str:
    df = metrics[
        (metrics["name"].str.startswith("replication_"))
        & (metrics["split_strategy"] == "augmented")
    ].copy()
    df = df.sort_values("mape")

    lines = [
        "    Backbone & Pooling & $N_{\\mathrm{test}}$ & MAE (\\degr) & "
        "RMSE (\\degr) & MAPE (\\%) & $R^2$ \\\\\n",
        "    \\midrule\n",
    ]
    for _, r in df.iterrows():
        lines.append(
            "    {bb} & {pool} & {n} & {mae} & {rmse} & {mape} & {r2} \\\\\n".format(
                bb=_label(BACKBONE_LABEL, r["backbone"]),
                pool=r["pooling"],
                n=int(r["n_test"]),
                mae=_fmt(r["mae"]),
                rmse=_fmt(r["rmse"]),
                mape=_fmt(r["mape"]),
                r2=_fmt(r["r2"], 3),
            )
        )
    body = "".join(lines)
    return _wrap_table(
        body,
        caption=(
            "Faithful replication under image-level sampling (the original "
            "augmented-corpus protocol): frozen ImageNet backbones with the "
            "orientation-preserving grid-pooling head. The best configuration "
            "(DenseNet201) reaches \\SI{5.84}{\\percent} MAPE, reproducing "
            "Table~I of the EMBC 2019 paper."
        ),
        label="tab:replication",
        colspec="llrrrrr",
    )


# --------------------------------------------------------------------------- #
# Table 2: methods progression on the honest patient-level protocol.
# --------------------------------------------------------------------------- #
def methods_progression_table(metrics: pd.DataFrame, era: pd.DataFrame) -> str:
    """Honest patient-level MAPE arc, all under patient 5-fold CV:
    GAP head -> grid pooling -> Optuna tuning -> ensemble. Every row is a logged
    era2019_cv.csv entry for frozen DenseNet201 (same backbone throughout), so the
    progression is apples-to-apples."""
    rows = []

    # The GAP baseline (global average pooling) the grid-pooling head replaces.
    gap = era[era["name"] == "gap_densenet201_patient"]
    if not gap.empty:
        r = gap.iloc[0]
        rows.append(
            ("DenseNet201, GAP head", _fmt(r["mape_mean"]), _fmt(r["mape_std"]),
             f"patient {int(r['k'])}-fold CV")
        )

    # Grid pooling, frozen (the SAME run that backs the bake-off + protocol figure).
    grid = era[era["name"] == "cmp_densenet201_patient"]
    if not grid.empty:
        r = grid.iloc[0]
        rows.append(
            ("DenseNet201, grid pooling (frozen)", _fmt(r["mape_mean"]),
             _fmt(r["mape_std"]), f"patient {int(r['k'])}-fold CV")
        )

    # Optuna-tuned (the SAME per-protocol row the dual-protocol table reports, so
    # the single tuned DenseNet201 number is consistent paper-wide).
    tuned_cv = era[era["name"] == "tuned_densenet201_patient"]
    if not tuned_cv.empty:
        r = tuned_cv.iloc[0]
        rows.append(
            ("DenseNet201, grid + Optuna-tuned", _fmt(r["mape_mean"]),
             _fmt(r["mape_std"]), f"patient {int(r['k'])}-fold CV")
        )

    lines = [
        "    Method & MAPE (\\%) & $\\pm$std & Protocol \\\\\n",
        "    \\midrule\n",
    ]
    for name, mape, std, proto in rows:
        std_s = "--" if std == "--" else f"$\\pm${std}"
        lines.append(
            f"    {name} & {mape} & {std_s} & {proto} \\\\\n"
        )
    # Ensemble headline (OOF mean + stacked) — RECOMPUTED from the tuned OOF
    # predictions via uda.evaluation.ensemble (not hand-typed), like every other table value.
    # The OOF ensemble is a single pooled out-of-fold prediction set, so there is no
    # per-fold std to report (rendered "--", not a blank cell).
    ens_mean, ens_stacked = _tuned_ensemble_mape()
    lines.append("    \\midrule\n")
    lines.append(
        f"    Tuned 5-model ensemble (OOF mean) & {ens_mean:.2f} & -- & patient 5-fold OOF \\\\\n"
    )
    lines.append(
        f"    \\textbf{{Tuned 5-model ensemble (stacked)}} & \\textbf{{{ens_stacked:.2f}}} & -- & "
        "patient 5-fold OOF \\\\\n"
    )
    body = "".join(lines)
    table = _wrap_table(
        body,
        caption=(
            "Methods progression for frozen DenseNet201 under the patient-level "
            "five-fold protocol (MAPE, \\%): the global-average-pooling (GAP) "
            "baseline, the orientation-preserving grid-pooling head, Optuna tuning "
            "of that head, and the five-model out-of-fold ensemble. The single-model "
            "rows are per-fold CV means; the ensemble rows are computed on the "
            "pooled out-of-fold predictions (so no per-fold std is defined, "
            "``--''). Aggregated the same pooled-OOF way, the tuned single "
            "DenseNet201 is \\SI{10.14}{\\percent} MAPE, so the genuine ensembling "
            "gain is \\SIrange{10.14}{8.53}{\\percent}; either way the ensemble "
            "breaks \\SI{10}{\\percent} on the cross-patient regime."
        ),
        label="tab:methods_progression",
        colspec="lrrl",
    )
    return table


# --------------------------------------------------------------------------- #
# Table 4: clinical-grade evaluation (recomputed from the OOF predictions).
# --------------------------------------------------------------------------- #
def clinical_table() -> str:
    # Conformal intervals at 90% and 95% nominal (patient-disjoint cal/test).
    c90 = evaluate_conformal(OOF_PRED, alpha=0.10)
    c95 = evaluate_conformal(OOF_PRED, alpha=0.05)

    # Bland-Altman vs the single reference reading (per-sample). agreement_from_csv
    # returns the reference-minus-method convention; we report method-minus-reference
    # so a NEGATIVE bias means the model reads lower than the reference (consistent
    # with the figure and the prose).
    ba = agreement_from_csv(OOF_PRED, agg="sample")
    ba_bias = -ba["bias"]
    ba_lo = -ba["loa_upper"]
    ba_hi = -ba["loa_lower"]

    # Rotation test-time augmentation: raw vs median reduction.
    tta_raw = tta_aggregate(OOF_PRED, reduce="circular_mean")  # provides n_base
    tta_med = tta_aggregate(OOF_PRED, reduce="median")
    # Raw (no-TTA) base-image MAE: de-rotated rows scored without reduction.
    # The no-reduction baseline is the raw figure; we recompute
    # it here as the per-row de-rotated MAE for traceability.
    import numpy as np
    df = pd.read_csv(OOF_PRED)
    base_est = df["theta_pred"].to_numpy(float) - df["rotation_deg"].to_numpy(float)
    base_true = df["theta_true"].to_numpy(float) - df["rotation_deg"].to_numpy(float)
    d = (base_true - base_est + 90.0) % 180.0 - 90.0
    raw_mae = float(np.mean(np.abs(d)))

    lines = [
        "    Quantity & Value & Detail \\\\\n",
        "    \\midrule\n",
        "    \\multicolumn{3}{l}{\\emph{Split-conformal prediction intervals "
        "(patient-disjoint cal/test)}} \\\\\n",
        f"    90\\% interval half-width & $\\pm${_fmt(c90['q'])}\\degr & "
        f"empirical coverage {_fmt(c90['coverage'] * 100, 1)}\\% \\\\\n",
        f"    95\\% interval half-width & $\\pm${_fmt(c95['q'])}\\degr & "
        f"empirical coverage {_fmt(c95['coverage'] * 100, 1)}\\% \\\\\n",
        "    \\midrule\n",
        "    \\multicolumn{3}{l}{\\emph{Bland--Altman, method $-$ reference "
        "(single reference reading)}} \\\\\n",
        f"    Bias & ${_fmt(ba_bias)}\\degr$ & "
        f"95\\% LoA ${_fmt(ba_lo)}$ \\ldots\\ "
        f"${_fmt(ba_hi)}\\degr$ \\\\\n",
        "    \\midrule\n",
        "    \\multicolumn{3}{l}{\\emph{Rotation test-time augmentation "
        "(base-image MAE)}} \\\\\n",
        f"    Raw (no TTA) & {_fmt(raw_mae)}\\degr & "
        f"{int(tta_raw['n_base'])} base images \\\\\n",
        f"    Median TTA & \\textbf{{{_fmt(tta_med['metrics']['mae'])}\\degr}} & "
        f"circular reduction over rotations \\\\\n",
    ]
    body = "".join(lines)
    return _wrap_table(
        body,
        caption=(
            "Clinical-grade evaluation of the tuned DenseNet201 on the "
            "patient-level OOF predictions. Split-conformal intervals achieve "
            "empirical coverage $\\geq$ nominal; the Bland--Altman bias is small "
            "and \\emph{negative} (model $-$ reference $<0$: the model reads "
            "slightly lower than the single reference reading); rotation TTA "
            "(median) nearly halves the raw base-image error. All numbers are "
            "recomputed from "
            "\\texttt{results/predictions/} by the \\texttt{uda} eval modules."
        ),
        label="tab:clinical",
        colspec="llp{0.42\\linewidth}",
    )


# --------------------------------------------------------------------------- #
# Dual sampling-protocol comparison — the headline of the replication+extension.
# Image-level vs patient-level, each Optuna-tuned to its own best, plus ensemble.
# --------------------------------------------------------------------------- #
_PROTO_OOF = {"patient": "tuned_{b}_oof.csv", "image": "tuned_{b}_image_oof.csv"}


def _oof_paths(protocol: str) -> list[str]:
    fname = _PROTO_OOF[protocol]
    return [str(ROOT / "results" / "predictions" / fname.format(b=b)) for b in _ENSEMBLE_BACKBONES]


def _ensemble_metrics(protocol: str, method: str) -> dict | None:
    """OOF-ensemble metrics for one protocol+method, recomputed from CSVs (or None)."""
    try:
        return ensemble_predictions(_oof_paths(protocol), method=method)["metrics"]
    except Exception:
        return None


def _tuned_cv(era: pd.DataFrame, backbone: str, protocol: str):
    """Protocol-tagged tuned k-fold CV row for a backbone (run_cv writes these), or None."""
    sub = era[era["name"] == f"tuned_{backbone}_{protocol}"]
    return sub.iloc[0] if not sub.empty else None


def dual_protocol_table(metrics: pd.DataFrame, era: pd.DataFrame) -> str:
    """Image-level vs patient-level, Optuna-tuned best per protocol (+ ensemble)."""

    def img_mape(b: str) -> float:
        r = _tuned_cv(era, b, "image")
        return float(r["mape_mean"]) if r is not None else 1e9

    backbones = sorted(_ENSEMBLE_BACKBONES, key=img_mape)

    def cell(r, key, nd=2):
        return _fmt(r[key], nd) if r is not None else "--"

    lines = [
        "    & \\multicolumn{3}{c}{Image-level sampling} "
        "& \\multicolumn{3}{c}{Patient-level sampling} \\\\\n",
        "    \\cmidrule(lr){2-4}\\cmidrule(lr){5-7}\n",
        "    Model (Optuna-tuned) & MAE (\\degr) & MAPE (\\%) & $R^2$ "
        "& MAE (\\degr) & MAPE (\\%) & $R^2$ \\\\\n",
        "    \\midrule\n",
    ]
    for b in backbones:
        ri, rp = _tuned_cv(era, b, "image"), _tuned_cv(era, b, "patient")
        if ri is None and rp is None:
            continue
        lines.append(
            "    {bb} & {im} & {imp} & {ir} & {pm} & {pmp} & {pr} \\\\\n".format(
                bb=_label(BACKBONE_LABEL, b),
                im=cell(ri, "mae_mean"), imp=cell(ri, "mape_mean"), ir=cell(ri, "r2_mean", 3),
                pm=cell(rp, "mae_mean"), pmp=cell(rp, "mape_mean"), pr=cell(rp, "r2_mean", 3),
            )
        )
    lines.append("    \\midrule\n")
    for method, lab in [("mean", "5-model ensemble (mean)"),
                        ("stacked", "\\textbf{5-model ensemble (stacked)}")]:
        ei, ep = _ensemble_metrics("image", method), _ensemble_metrics("patient", method)

        def ecell(m, key, nd=2):
            return _fmt(m[key], nd) if m is not None else "--"

        lines.append(
            "    {lab} & {im} & {imp} & {ir} & {pm} & {pmp} & {pr} \\\\\n".format(
                lab=lab,
                im=ecell(ei, "mae"), imp=ecell(ei, "mape"), ir=ecell(ei, "r2", 3),
                pm=ecell(ep, "mae"), pmp=ecell(ep, "mape"), pr=ecell(ep, "r2", 3),
            )
        )
    body = "".join(lines)
    return _wrap_table(
        body,
        caption=(
            "Doppler-angle estimation under two sampling protocols, each "
            "Optuna-tuned to its own best configuration. \\emph{Image-level "
            "sampling} draws the train/test partition over the rotation-augmented "
            "image corpus (the standard synthetic-data protocol of the original "
            "study); \\emph{patient-level sampling} holds out whole volunteers, a "
            "stricter cross-subject generalization test. Per-backbone rows are "
            "patient/image five-fold cross-validated tuned heads (means; per-fold "
            "MAPE std is \\SIrange{0.1}{0.7}{\\percent} for image-level and "
            "\\SIrange{2.6}{4.4}{\\percent} for the higher-variance patient-level "
            "folds); ensemble rows are pooled "
            "out-of-fold over the five tuned models. Note the aggregation differs: "
            "member rows are per-fold means, ensemble rows are pooled OOF (the "
            "single tuned DenseNet201 aggregated pooled-OOF is \\SI{7.80}{\\degree} "
            "MAE patient-level, the like-for-like predecessor of the ensemble "
            "rows). Both columns are recomputed "
            "from \\texttt{results/} by \\texttt{scripts/gen\\_paper\\_tables.py}."
        ),
        label="tab:dual_protocol",
        colspec="lrrrrrr",
    )


def copy_figures() -> list[str]:
    FIG_DST.mkdir(parents=True, exist_ok=True)
    copied = []
    for fig in FIGURES:
        src = FIG_SRC / fig
        if not src.exists():
            raise FileNotFoundError(f"missing figure: {src}")
        shutil.copy2(src, FIG_DST / fig)
        copied.append(str(FIG_DST / fig))
    return copied


def main() -> None:
    TABLES.mkdir(parents=True, exist_ok=True)
    metrics = pd.read_csv(METRICS)
    era = pd.read_csv(ERA_CV)

    outputs = {
        "replication.tex": replication_table(metrics),
        "dual_protocol.tex": dual_protocol_table(metrics, era),
        "methods_progression.tex": methods_progression_table(metrics, era),
        "clinical.tex": clinical_table(),
    }
    for fname, content in outputs.items():
        (TABLES / fname).write_text(content)
        print(f"wrote {TABLES / fname}")

    for path in copy_figures():
        print(f"copied {path}")


if __name__ == "__main__":
    main()
