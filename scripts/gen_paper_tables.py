r"""Generate the paper's booktabs LaTeX tables from results/ (reproducible).

Every number written under ``paper/tables/`` is read from ``results/metrics.csv``
and ``results/era2019_cv.csv``, or recomputed from the saved OOF predictions by
calling the Keras-free eval modules (``uda.evaluation.conformal``, ``uda.evaluation.agreement``,
``uda.evaluation.tta``). Nothing is hand-typed. This script ALSO copies the figures the
paper ``\includegraphics`` into ``paper/figures/`` so the LaTeX source is
self-contained for arXiv.

Run:  ``pixi run python scripts/gen_paper_tables.py``

Outputs:
  paper/tables/replication.tex
  paper/tables/methods_progression.tex
  paper/tables/clinical.tex
  paper/figures/*.png  (copied from results/figures/)
"""
from __future__ import annotations

import shutil
from pathlib import Path

import pandas as pd

# uda eval modules: recompute clinical numbers from saved predictions, never
# hand-type them. These are Keras-free post-hoc analyses of the OOF CSV.
from uda.evaluation.agreement import agreement_from_csv
from uda.evaluation.conformal import evaluate_conformal
from uda.evaluation.ensemble import ensemble_predictions
from uda.evaluation.tta import tta_aggregate

_ENSEMBLE_BACKBONES = ["densenet201", "resnet50", "vgg19", "xception", "inceptionv3"]


def _tuned_ensemble_mape() -> tuple[float, float]:
    """(mean, stacked) OOF-ensemble MAPE recomputed from the tuned OOF predictions."""
    paths = [str(ROOT / "results" / "predictions" / f"tuned_{b}_oof.csv") for b in _ENSEMBLE_BACKBONES]
    mean = ensemble_predictions(paths, method="mean")["metrics"]["mape"]
    stacked = ensemble_predictions(paths, method="stacked")["metrics"]["mape"]
    return float(mean), float(stacked)

